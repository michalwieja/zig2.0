            <h2>STATUT ZAGŁĘBIOWSKIEJ IZBY GOSPODARCZEJ
              (tekst ujednolicony 2020)</h2>

            <small>Dąbrowa Górnicza, dn. 09.07.2020r. </small>

            <h3>Rozdział I</h3>
            <h4>Postanowienia ogólne</h4>

            <h5>§ 1</h5>

            <p> 1. Zagłębiowska Izba Gospodarcza, zwana dalej Izbą, jest dobrowolną samorządową
              organizacją osób prawnych, fizycznych i jednostek organizacyjnych nie posiadających
              osobowości prawnej, które prowadzą działalność gospodarczą na terenie Rzeczypospolitej
              Polskiej.</p>
            <p> 2. Izba dział w oparciu o Ustawę o Izbach Gospodarczych z dnia 30 maja 1989 roku (Dz.
              U.
              1989.35.195 wraz z późniejszymi zmianami).</p>
            <h5> § 2</h5>
            <p> Izba może używać nazwy skróconej – „ZIG”.</p>
            <h5> § 3</h5>
            <p> 1. Siedzibą Izby jest miasto Dąbrowa Górnicza.
            </p>
            <p> 2. Izba działa na obszarze Rzeczypospolitej Polskiej a także, gdy realizacja jej celów
              tego wymaga – poza granicami Polski.</p>
            <p> 3. Izba może tworzyć oddziały i przedstawicielstwa.
            </p>
            <p> 4. Izba może zawierać celowe porozumienia i może być członkiem krajowych oraz
              zagranicznych organizacji o podobnych zadaniach statutowych, a także uczestniczyć w
              spółkach prawa handlowego, ustanawiać fundacje i być członkiem stowarzyszeń, innych
              organizacji oraz ruchów społecznych i gospodarczych.</p>

            <h5>§ 4</h5>
            <p> 1. Izba może prowadzić działalność gospodarczą.
            </p>
            <p> 2. Izba może prowadzić działalność charytatywną, w szczególności na rzecz osób, rodzin
              i
              grup społecznych najbardziej potrzebujących.</p>
            <p> 3. Izba może tworzyć fundusze celowe.
            </p>

            <h3> Rozdział II
            </h3><h4> Cele i sposoby ich realizacji</h4>

            <h5> § 5
            </h5>
            <p> 1. Reprezentowanie interesów członków Izby, podmiotów gospodarczych i samorządu
              gospodarczego wobec organów rządowych i samorządowych oraz organizacji krajowych i
              zagranicznych.</p>
            <p> 2. Udział w tworzeniu i zmianie prawa w zakresie gospodarki, zgodnie z interesami
              członków Izby i samorządu gospodarczego.
            </p>
            <p>3. Działanie na rzecz rozwoju przedsiębiorczości.</p>
            <p> 4. Współudział w tworzeniu warunków sprzyjających działalności gospodarczej w tym
              nowatorskich form działania.
            </p>
            <p> 5. Inspirowanie tworzenia i nowelizacji przepisów oraz prezentowanie opinii w
              dziedzinie polityki gospodarczej i społecznej.</p>
            <p> 6. Kształtowanie i upowszechnianie zasad etyki i społecznie akceptowanych norm
              postępowania w stosunkach gospodarczych.
            </p>
            <p>7. Promowanie członków Izby w kraju i za granicą.</p>
            <p> 8. Organizowanie szkoleń i doradztwa gospodarczego.
            </p>
            <p> 9. Tworzenie warunków do rozstrzygania sporów w drodze postępowania polubownego.
            </p>
            <h5> § 6
            </h5>
            <p> Izba realizuje swoje zadania poprzez:</p>
            <p> 1. Występowanie w imieniu i na rzecz członków Izby oraz samorządu gospodarczego przed
              organami państwowymi, samorządowymi, instytucjami, organizacjami oraz ruchami
              gospodarczymi.
            </p>
            <p> 2. Wyrażanie opinii o projektach rozwiązań prawnych i organizacyjnych w
              zakresie gospodarki.
            </p>
            <p> 3. Współpracę i udział w przygotowywaniu projektów aktów prawnych w zakresie
              gospodarki przez organy administracji samorządowej i państwowej oraz instytucji
              doradczo-opiniodawczych.
            </p>
            <p> 4. Współpracę z innymi gospodarczymi organizacjami regionalnymi, krajowymi oraz
              zagranicznymi.
            </p>
            <p> 5. Świadczenie na rzecz członków Izby i podmiotów gospodarczych pomocy, w różnych
              formach i zakresie w podejmowaniu i prowadzeniu działalności.
            </p>
            <p> 6. Tworzenie i prowadzenie systemów informacji gospodarczej.
            </p>
            <p> 7. Wykonywanie zadań w zakresie współpracy z zagranicą.
            </p>
            <p> 8. Udzielanie pomocy organizacyjnej i doradczo - konsultacyjnej, inspirowanie i pomoc
              organizacyjna w działalności marketingowej i promocji.
            </p>
            <p> 9. Prowadzenie działalności szkoleniowej i wydawniczej.
            </p>
            <p> 10. Delegowanie przedstawicieli do organów doradczych władz i administracji.
            </p>
            <p> 11. Organizowanie wystaw, targów i pokazów.
            </p>
            <p> 12. Organizowanie szkoleń, doradztwa ekonomicznego, prawnego i organizacyjnego.
            </p>
            <p> 13. Wdrażanie i prowadzenie postępowania mediacyjnego i honorowego w sporach
              gospodarczych, których stronami są członkowie Izby.
            </p>
            <p>
              14. Tworzenie branżowych i środowiskowych klubów członkowskich.

            </p>
            <p> 15. Tworzenie zespołów ekspertów, komisji i zespołów problemowych.
            </p>
            <p> 16. Tworzenie fundacji, funduszy i stypendiów dla popierania inicjatyw gospodarczych.
            </p>
            <p> 17. Przyczynianie się do rozwoju lokalnej oraz regionalnej infrastruktury
              przedsiębiorczości.
            </p>


            <h5> § 7
            </h5>
            <p> Izba prowadzi działalność odpłatną i nieodpłatną, w szczególności:
            </p>
            <p> 1. Pozostałe drukowanie.
            </p>
            <p> 2. Działalność usługowa związana z przygotowywaniem do druku
            </p>
            <p> 3. Wydawanie książek
            </p>
            <p> 4. Wydawanie gazet
            </p>
            <p> 5. Wydawanie czasopism i pozostałych periodyków
            </p>
            <p> 6. Pozostała działalność wydawnicza
            </p>
            <p> 7. Działalność portali internetowych
            </p>
            <p> 8. Działalność agencji informacyjnych
            </p>
            <p> 9. Pozostała działalność usługowa w zakresie informacji, gdzie indziej
              niesklasyfikowana
            </p>
            <p> 10. Działalność prawnicza
            </p>
            <p> 11. Działalność rachunkowo-księgowa; doradztwo podatkowe
            </p>
            <p> 12. Stosunki międzyludzkie (public relations) i komunikacja
            </p>
            <p> 13. Pozostałe doradztwo w zakresie prowadzenia działalności gospodarczej i zarządzania
            </p>
            <p> 14. Pozostałe pozaszkolne formy edukacji, gdzie indziej niesklasyfikowane
            </p>
            <p> 15. Działalność wspomagająca edukację
            </p>
            <p> 16. Pozostała działalność rozrywkowa i rekreacyjna
            </p>
            <p> 17. Działalność organizacji komercyjnych i pracodawców
            </p>
            <p> 18. Działalność organizacji profesjonalnych
            </p>
            <p> 19. Działalność pozostałych organizacji członkowskich, gdzie indziej niesklasyfikowana
            </p>
            <p> 20. Pozostała działalność usługowa, gdzie indziej niesklasyfikowana
            </p>
            <p> 21. Pozostała sprzedaż detaliczna prowadzona w niewyspecjalizowanych sklepach
            </p>
            <p> 22. Sprzedaż detaliczna książek prowadzona w wyspecjalizowanych sklepach
            </p>
            <p> 23. Sprzedaż detaliczna prowadzona przez domy sprzedaży wysyłkowej lub Internet
            </p>
            <p> 24. Sprzedaż detaliczna pozostałych nowych wyrobów prowadzona w wyspecjalizowanych
              sklepach
            </p>
            <p> Zagłębiowska Izba Gospodarcza realizuje działania również poprzez współpracę ze
              społecznościami, instytucjami i organizacjami krajowymi i zagranicznymi.
            </p>

            <h3> Rozdział III
            </h3>
            <h4> Członkowie Izby, ich prawa i obowiązki
            </h4>
            <h5> § 8
            </h5>
            <p> 1. Członkiem Izby może być każdy podmiot prowadzący działalność gospodarczą, samorządy
              terytorialne i inne organizacje prowadzące działalność gospodarczą zainteresowane
              realizacją celów określonych w niniejszym statucie.
            </p>
            <p> 2. Członkami-Założycielami Izby są samorządy terytorialne, przedsiębiorstwa
              przemysłowe oraz firmy handlowe i usługowe z terenu województwa śląskiego, które
              przystąpiły do Izby przed jej rejestracją i posiadają w niej nieprzerwane członkostwo.
            </p>
            <h5> § 9
            </h5><p>
            1) Decyzje w sprawach przyznania członkostwa podejmuje Dyrektor Izby po rozpatrzeniu
            wnio­sku zainteresowanego podmiotu.
            </p>


            <p> 2) Zainteresowany podmiot gospodarczy ubiegający się o przyjęcie w poczet członków
              Izby winien złożyć deklarację członkowską, ankietę informacyjną i zobowiązanie do
              płacenia składek członkowskich.
            </p>
            <p> 3) Dyrektor Izby nie może odmówić przyjęcia w poczet członków podmiotu gospodarczego,
              który spełnia wymogi ustawowe i statutu.
            </p>
            <p> 4) Od odmownej decyzji w sprawie przyjęcia w poczet członków Izby służy
              zainteresowanemu podmiotowi odwołanie do Walnego Zgromadzenia w ciągu 30 dni od
              otrzymania decyzji.
            </p>

            <h5>§ 10</h5>
            <p> 1) Członkowie Izby wykonują swoje prawa i obowiązki osobiście lub przez pełnomocnika.
            </p>
            <p> 2) Pełnomocnictwo powinno być udzielone na piśmie pod rygorem nieważności i dołączone
              do protokołu.
            </p>
            <h5> § 11
            </h5>
            <p>Obowiązkiem członków Izby jest:</p>


            <p> 1) Postępowanie zgodne ze Statutem, regulaminami i uchwałami organów Izby.
            </p>
            <p> 2)  Czynny udział w realizacji zadań statutowych Izby.
            </p>
            <p> 3)  Postępowanie zgodne z zasadami dobrych obyczajów we wzajemnych stosunkach
              członkowskich oraz w obrocie gospodarczym.
            </p>
            <p> 4)  Ochrona dobrego imienia Izby.
            </p>
            <p> 5) Regularnego opłacania składek członkowskich.
            </p>
            <p> 6)  Powstrzymywanie się od działalności na szkodę Izby.
            </p>

            <h5> § 12</h5>
            <p> 1. Członkowie posiadają czynne i bierne prawo wyborcze, a nadto mają prawo do:
            </p>
            <p> A) wyrażania swoich opinii i propozycji dotyczących działalności Izby;
            </p>
            <p> B) składania odwołań od uchwał Rady w sprawach ich dotyczących w przypadkach
              określonych w Statucie;
            </p>
            <p> C) korzystania z urządzeń, świadczeń i pomocy Izby;
            </p>
            <p> D) grupowania się w zespołach i komisjach stałych lub doraźnych ,korzystania ze znaku
              firmowego Izby, według zasad określonych przez Radę Izby.
            </p>

            <p> 2. Członkostwo w Izbie wygasa w przypadku : ustania, skreślenia, wykluczenia lub
              z innych ważnych przyczyn.</p>

            <p> 3. Członkostwo ustaje na skutek:
            </p>
            <p> A) pisemnej rezygnacji złożonej do Rady Izby;
            </p>
            <p> B) utraty pełnej zdolności do czynności prawnych;
            </p>
            <p> C) śmierci członka lub jego wykreślenia z właściwego rejestru lub ewidencji.
            </p>

            <p> 4. Skreślenie członka następuje w szczególności w przypadku:
            </p>
            <p> A) prowadzenia przez członka działalności sprzecznej ze Statutem;
            </p>
            <p> B) prowadzenia przez członka działalności na szkodę Izby;
            </p>
            <p> C) zalegania z zapłatą zobowiązań wynikających z przynależności do Izby za cztery
              okresy rozliczeniowe, co nie zwalnia członka z obowiązku uregulowania zadłużenia wobec
              Izby;
            </p>
            <p> D) utraty praw obywatelskich na mocy prawomocnego wyroku sądu;
            </p>
            <p> E) skazania członka Izby prawomocnym wyrokiem za przestępstwa określone w przepisach
              Kodeksu Karnego;
            </p>
            <p> F) gdy dalszego członkostwa przedsiębiorcy nie da się pogodzić z celami Izby.
            </p>

            <p> 5. Skreślenie staje się skuteczne z chwilą podjęcia postanowienia przez Dyrektora Izby
              i doręczenia członkowi zawiadomienia o skreśleniu wraz z pouczeniem o trybie i terminie
              wniesienia odwołania. Zawiadomienia dokonuje się w formie pisemnej w terminie 14 dni od
              podjęcia odpowiedniej w tym zakresie uchwały i doręcza się członkowi za pokwitowaniem
              lub przez pocztę listem poleconym. Zawiadomienie niedoręczone, zwrócone na skutek nie
              zgłoszenia przez członka zmiany podanego przez niego wcześniej adresu, ma moc prawną
              doręczenia.
            </p>
            <p> 6. Skreślonemu członkowi przysługuje prawo odwołania do Walnego Zgromadzenia w
              terminie 30 dni od dnia otrzymania zawiadomienia o skreśleniu oraz prawo uczestniczenia
              w jego obradach przy rozpatrywaniu odwołania i jego popieranie.
            </p>
            <p> 7. W przypadku złożenia odwołania, Rada Izby w terminie 60 dni od dnia doręczenia
              odwołania podejmuje uchwałę o zwołaniu Walnego Zgromadzenia lub uzupełnieniu porządku
              obrad w zwołanym już Zgromadzeniu. Walne Zgromadzenie, na którym rozpatrywane będzie
              odwołanie nie może odbyć się później niż  6 miesięcy od dnia doręczenia odwołania.
            </p>
            <p> 8. W razie przekształcenia członka Izby, nowo powstały podmiot staje się członkiem
              Izby z chwilą ujawnienia w KRS zmiany podmiotowej pod warunkiem złożenia deklaracji
              członkowskiej. Postanowienie zdania pierwszego  nie dotyczy połączenia ani podziału – w
              takim przypadku nowo powstały podmiot może się ubiegać o członkostwo na zasadach
              ogólnych. A członkostwo starego wygasa z w/w ważnych przyczyn. Decyzję w powyższym
              podejmuje Dyrektor ZIG a jego decyzja nie podlega zaskarżeniu do Rady Izby i jest
              natychmiast wykonalna.
            </p>
            <p> 9. Jeżeli utrata członkostwa nastąpiła z mocy samego prawa lub postanowienia statutu,
              ustanie członkostwa zostanie stwierdzone deklaratoryjną decyzją Dyrektora Izby. Decyzja
              Dyrektora Izby nie podlega zaskarżeniu do Rady Izby i jest natychmiast wykonalna. Są to
              ważne przyczyny wygaśnięcia członkostwa.
            </p>
            <p> 10. Wykluczenie członka następuje decyzją Dyrektora Izby w przypadku naruszenia
              zapisów niniejszego statutu w szczególności § 11.
            </p>
            <p> 11. Wykluczonemu członkowi przysługuje prawo odwołania do Walnego Zgromadzenia w
              terminie 30 dni od dnia otrzymania zawiadomienia o skreśleniu oraz prawo uczestniczenia
              w jego obradach przy rozpatrywaniu odwołania i jego popieranie.
            </p>
            <p> 12. W przypadku złożenia odwołania, Rada Izby w terminie 60 dni od dnia doręczenia
              odwołania podejmuje uchwałę o zwołaniu Walnego Zgromadzenia lub uzupełnieniu porządku
              obrad w zwołanym już Zgromadzeniu. Walne Zgromadzenie, na którym rozpatrywane będzie
              odwołanie nie może odbyć się później niż  6 miesięcy od dnia doręczenia odwołania.
            </p>

            <h3> ROZDZIAŁ IV
            </h3>

            <h4> Organy Izby
            </h4>
            <h5> § 13
            </h5>
            <p> 1. Władzami Izby są:
            </p>
            <p> A.    Walne Zgromadzenie Członków Izby
            </p>
            <p> B.    Rada Izby
            </p>
            <p> C.    Dyrektor Izby
            </p>

            <p> 2. Kadencja  wszystkich organów oraz delegatów trwa 4 lata i kończy się z chwilą
              wyboru nowych organów.
            </p>
            <p> 3. Wybór organów Izby odbywa się w drodze głosowania tajnego z nieograniczonej liczby
              kandydatów i dokonywany jest wyłącznie przez członków Izby (prawidłowo
              reprezentowanych).
            </p>
            <p> 4. Członkowie wszystkich wybieralnych organów Izby nie mogą być skazani prawomocnym
              wyrokiem za przestępstwo umyślne. Powinni w tym zakresie złożyć stosowne oświadczenie
              wraz ze zgodą na kandydowanie.
            </p>
            <p> 5. Członkostwo w organie Izby oraz mandat delegata wygasają na skutek:
            </p>
            <p> A) pisemnej rezygnacji skierowanej do Rady Izby lub Prezesa Izby;
            </p>
            <p> B) śmierci;
            </p>
            <p> C) wygaśnięcia członkostwa w Izbie jakiegokolwiek członka organu lub delegata będącego
              jednocześnie członkiem Izby, a w szczególności w przypadku wygaśnięcia członkostwa w
              Izbie z przyczyn wskazanych w art. 12 ust. 3 lub 4 Statutu;
            </p>
            <p> D) wygaśnięcia stosunku prawnego pomiędzy członkiem organu lub delegatem a członkiem
              Izby, o którym mowa w art. 10; chyba , że Rada Izby  postanowi inaczej a członek Izby 
              nie zgłosi pisemnego sprzeciwu;
            </p>
            <p> E) upływu kadencji organu;
            </p>
            <p> F) odwołania w przypadkach określonych Statutem;
            </p>
            <p> G) nieudzielenia przez Walne Zgromadzenie absolutorium z wykonywanych obowiązków;
            </p>
            <p> H) w razie wygaśnięcia członkostwa w Izbie podmiotu, którego przedstawicielem była
              osoba pełniąca funkcję w organie Izby lub funkcję delegata.
            </p>
            <p> 6. W przypadku konieczności uzupełnienia składu osobowego organu Izby z uwagi na
              wygaśnięcie członkostwa danego członka organu w trakcie trwania kadencji, Rada Izby
              podejmuje uchwałę o wyborze nowego członka organu. Wybór nowego członka organu
              dokonywany jest w pierwszej kolejności spośród nie wybranych kandydatów do danego organu
              Izby w kolejności ze względu na liczbę uzyskanych głosów. W razie równej liczby głosów
              Rada Izby przeprowadza losowanie. W przypadku braku kandydatów o których mowa w zdaniach
              poprzedzających, Rada Izby może powołać nowego członka według własnego uznania. Nowy
              członek organu wybrany przez Radę Izby jest zatwierdzany przez najbliższe Walne
              Zgromadzenie.
            </p>
            <h5> § 14
            </h5>
            <p> 1. Nie uchybiając innym postanowieniom niniejszego Statutu, z ważnych przyczyn (w tym
              w szczególności z powodu nieprzestrzegania Regulaminu pracy organów Izby, niestosowania
              się do uchwał organów Izby bądź prowadzenia działalności sprzecznej ze Statutem lub na
              szkodę Izby) w czasie trwania kadencji, poszczególni członkowie organów Izby mogą być
              zawieszeni w składzie tych organów. Uchwałę w przedmiocie zawieszenia podejmuje Rada
              Izby, większością 2/3 głosów przy obecności co najmniej połowy członków Rady Izby.
              Zawieszenie trwa do czasu  podjęcia uchwały przez Walne Zgromadzenie w przedmiocie
              odwołania zawieszonego członka organu, lecz nie dłużej niż 6 miesięcy od dnia
              zawieszenia.
            </p>
            <p> 2. O ile postanowienia Statutu nie stanowią inaczej uchwały wszystkich organów Izby
              zapadają zwykłą większością głosów w głosowaniu jawnym. Wyłączenie jawności głosowania
              nastąpić może w każdej sprawie na wniosek co najmniej 1/5 obecnych uczestników
              uprawnionych do głosowania.
            </p>
            <h4> Walne Zgromadzenie
            </h4>
            <h5> § 15
            </h5>
            <p> 1. Walne Zgromadzenie jest najwyższą władzą Izby.
            </p>
            <p> 2. Walne Zgromadzenie może być zwoływane w trybie zwyczajnym lub nadzwyczajnym.
            </p>

            <h5> § 16
            </h5>
            <p> 1. Zwyczajne Walne Zgromadzenie zwoływane jest przez Radę Izby i jego posiedzenie
              odbywa się co najmniej raz do roku.
            </p>
            <p> 2. Zwyczajne Walne Zgromadzenie zwoływane jest jako Zgromadzenie Sprawozdawczo -
              Wyborcze co 4 lata, a jako Zgromadzenie Sprawozdawcze co roku - do końca drugiego
              kwartału danego roku.
            </p>
            <p> 3. Rokiem sprawozdawczym jest rok liczony od ostatniego sprawozdawczego Walnego
              Zgromadzenia, a rokiem obrachunkowym jest  rok kalendarzowy.
            </p>


            <h5> § 17
            </h5>
            <p> 1. Nadzwyczajne Walne Zgromadzenie zwołuje Rada z własnej inicjatywy, bądź na żądanie
              Dyrektora Izby lub na pisemny wniosek 1/10 członków Izby.
            </p>
            <p> 2. Nadzwyczajne Walne Zgromadzenie powinno odbyć się nie później niż w ciągu 14 dni od
              daty zgłoszenia wniosku (żądania) i być poświęcone włącznie sprawom, dla których zostało
              zwołane.
            </p>
            <h5> § 18
            </h5>
            <p> 1. Zawiadomienia o terminie, miejscu i proponowanym porządku obrad wysyłane są przez
              Radę Izby do wszystkich członków Izby z co najmniej 14-dniowym wyprzedzeniem w stosunku
              do terminu posiedzenia Walnego Zgromadzenia.
            </p>
            <p> 2. Wnioski członków Izby w przedmiocie zmian lub uzupełnienia porządku obrad winny być
              składane w Biurze Zarządu Izby najpóźniej w terminie 7 dni przed terminem posiedzenia
              Walnego Zgromadzenia. Biuro Zarządu ma obowiązek zawiadomić członków o proponowanych
              zmianach najpóźniej na 3 dni przed terminem Walnego Zgromadzenia.
            </p>
            <p> 3. O Zmianach i uzupełnieniach porządku obrad  decyduje Walne Zgromadzenie w
              głosowaniu jawnym zwykłą większością głosów. 
            </p>
            <h5> § 19
            </h5>
            <p> 1.  W Walnym Zgromadzeniu członkowie Izby uczestniczą osobiście lub przez
              upełnomocnionych przedstawicieli.
            </p>
            <p> 2.  W Walnym Zgromadzeniu uczestniczy Dyrektor Izby bez prawa do głosowania.
            </p>
            <p> 3.  Każdy członek Izby dysponuje na Walnym Zgromadzeniu jednym głosem.
            </p>
            <p> 4. Walne Zgromadzenie podejmuje uchwały w zwykłych sprawach zwykłą większością głosów
              natomiast w sprawie zmiany statutu Izby oraz rozwiązania Izby większością 2/3 głosów
              oddanych przy obecności co najmniej połowy uprawnionych do głosowania w pierwszym
              terminie, a w drugim terminie może być podjęta bez względu na liczbę obecnych  członków.
            </p>
            <p> 5. W razie braku kworum organ zwołujący zgromadzenie wyznacza drugi termin jego
              od­bycia. Walne Zgromadzenie odbyte w drugim terminie jest zdolne do podejmowania uchwal
              bez względu na liczbę obecnych uprawnionych do głosowania.
            </p>


            <h5> § 20
            </h5>
            <p> Do  wyłącznej kompetencji  Walnego Zgromadzenia należy:
            </p>
            <p> 1. uchwalanie Statutu Izby i zmian w Statucie,
            </p>
            <p> 2. uchwalanie regulaminu obrad Walnego Zgromadzenia,
            </p>
            <p> 3. wybór i odwołanie Prezesa i członków Rady,
            </p>
            <p> 4. wytyczanie kierunków działalności Izby,
            </p>
            <p> 5. zatwierdzanie sprawozdań z działalności Rady Izby oraz rocznego bilansu,
            </p>
            <p> 6. udzielanie absolutorium Prezesowi i Radzie Izby,
            </p>
            <p> 7.  podejmowanie uchwał w sprawie sprzedaży i zakupu nieruchomości, wyodrębnionych
              zakładów gospodarczych, oraz innych walorów majątkowych,
            </p>
            <p> 8. ustalanie zasad wynagradzania członków Rady Izby,
            </p>
            <p> 9. podejmowanie uchwał w innych sprawach  wynikających ze Statutu Izby,
            </p>
            <p> 10. Ustalanie wysokości składek członkowskich.
            </p>

            <h4>
            </h4>
            <h5> § 21
            </h5>
            <p> 1. Rada Izby kieruje działalnością Izby.
            </p>
            <p> 2. Rada Izby za swoje działania odpowiada przed Walnym Zgromadzeniem.
            </p>
            <p> 3. Rada Izby uchwala swój regulamin pracy.
            </p>

            <h5> § 22
            </h5>
            <p> 1.  Rada Izby składa się z Prezesa i 4 członków wybieranych  przez Walne Zgromadzenie.
            </p>
            <p> 2.  Członkowie Rady wybierają ze swojego grona  wiceprezesa.
            </p>
            <p> 3.  Prezes Izby przewodniczy Radzie Izby, podpisując wszystkie uchwały.
            </p>
            <p> 4.  Kadencja Rady Izby trwa cztery lata. Upływa po czwartym roku jej funkcjonowania, w
              momencie zwołania kolejnego sprawozdawczego Walnego Zgromadzenia Członków Izby.  
            </p>
            <p> 5.  W posiedzeniach Rady uczestniczy także Dyrektor Izby, bez prawa głosu.
            </p>
            <p> Jeśli Rada ustąpi bądź utraci zdolność działania, obowiązki Prezesa przejmuje Dyrektor
              ZIG. W takim przypadku Dyrektor jest obowiązany zwołać Walne Zgromadzenie  w najbliższym
              możliwym terminie, nie później jednak niż w ciągu 14 dni kalendarzowych w taki sposób by
              odbyło się w ciągu 30 dni od tego zdarzenia. 
            </p>
            <h5> § 23
            </h5>
            <p> 1. Rada powołuje stałe komisje:
            </p>
            <p> a.    Komisję Samorządową,
            </p>
            <p> b.    Komisję Inicjatyw Gospodarczych
            </p>
            <p> 2. Komisje stałe są organem doradczym Rady przy opracowywaniu i realizacji określonych
              zadań. Komisje działają zgodnie z regulaminem opracowanym i zatwierdzonym przez Radę
              Izby.
            </p>
            <p> 3. Członkowie komisji wybierają ze swego grona przewodniczącego. Przewodniczący 
              komisji staje się automatycznie członkiem Rady ZIG. Oprócz brania aktywnego udziału w
              posiedzeniach Rady Przewodniczący komisji zobowiązani są do utrzymywania stałej roboczej
              współpracy z Biurem ZIG.
            </p>
            <p> 4. Rada może powoływać komisje doraźne w zależności od potrzeb bieżących. Zasady
              działania tych komisji ustala Rada w formie regulaminu.
            </p>
            <p> 5. Przewodniczących Komisji wybiera się spośród członków Rady Izby.
            </p>
            <p> 6. W przypadku pozbawienia członkostwa oraz cofnięcia mandatu do reprezentowania osoby
              prawnej dla  Prezesa lub członka Rady Izby, obowiązki Prezesa przejmuje Wiceprezes.
            </p>
            <p> 7. W takim przypadku wybór nowego Prezesa i członków Rady winien być przeprowadzony w 
              formie  wyborów  uzupełniających  na  najbliższym  posiedzeniu Walnego Zgromadzenia.
            </p>
            <h5> § 24
            </h5>
            <p> 1. Do kompetencji i obowiązków Rady Izby należy:
            </p>

            <p> A) wykonywanie uchwał Walnego Zgromadzenia,
            </p>
            <p> B) dokonywanie wyboru biegłego rewidenta do badania ksiąg rachunkowych Izby,
              C) tworzenie i likwidacja komisji stałych i doraźnych oraz organów doradczych i
              opiniodawczych Izby, jak również sprawowanie nad nimi nadzoru i nadawanie regulaminu
              organizacyjnego,
            </p>
            <p> D) powoływanie i likwidacja oddziałów i przedstawicielstw Izby oraz zatwierdzanie
              organizacji wewnętrznej i zasad wyborów władz oddziałów i przedstawicielstw Izby,
            </p>
            <p> E) zwoływanie i proponowanie porządku obrad Walnych Zgromadzeń,
            </p>
            <p> F) składanie Walnemu Zgromadzeniu sprawozdania ze swojej działalności,
            </p>
            <p> G) wnioskowanie Walnemu Zgromadzeniu odwołania  członka Rady w przypadku
              niewypełniania przez niego obowiązków członka Rady,
            </p>
            <p> H) podejmowanie uchwał  w sprawie przystępowania Izby do spółek prawa handlowego,
            </p>
            <p> I)  rozpatrywanie uchwał w sprawach członkowskich, w tym rozpatrywanie odwołań
              członków od decyzji Dyrektora Izby o wykreśleniu z listy członków ZIG, oraz podejmowanie
              uchwał o wykluczeniu członka,
            </p>
            <p> J)  powołanie i odwoływanie Dyrektora Izby,
            </p>
            <p> K)  inne zadania wynikające wprost ze Statutu .
            </p>

            <p> 2. Rada Izby może korzystać z opinii i doradztwa rzeczoznawców.
            </p>

            <h5> § 25
            </h5>
            <p> 1.  Rada Izby działa na podstawie uchwalonego przez siebie regulaminu określającego
              jej organizację i sposób wykonywania czynności.
            </p>
            <p> 2.  Decyzje i uchwały Rady zapadają zwykłą większością głosów przy obecności minimum 
              50% liczby członków Rady.
            </p>
            <p> 3.  W przypadku równowagi głosów popierających i przeciwnych podczas głosowania, głos
              Prezesa Rady liczy się podwójnie.
            </p>


            <h4> Prezydium Rady Izby
            </h4>
            <h5> § 26
            </h5>
            <p> 1. W skład Prezydium Rady Izby wchodzi:
            </p>
            <p> a) Prezes Rady
            </p>
            <p> b) Wiceprezes Rady
            </p>
            <p> c) Dyrektor ZIG z głosem doradczym.
            </p>

            <p> 2. Do obowiązków Prezydium Rady należy:
            </p>
            <p> A) ustalanie rocznego programu działania Izby
            </p>
            <p> B) opracowanie budżetu Izby i zatwierdzanie preliminarzy budżetowych,
            </p>
            <p> C) powoływanie funduszy celowych w porozumieniu z Dyrektorem.
            </p>
            <p> D) podpisywanie uchwalonego przez Walne Statutu ZIG.
            </p>

            <p> Dyrektor Izby
            </p>

            <h5> § 27
            </h5>
            <p> 1.  Dyrektor Izby jest jednoosobowym organem zarządzającym, odpowiedzialnym za wszelką
              działalność bieżącą Izby nie zastrzeżoną dla innych organów Izby.
            </p>
            <p> 2.    Dyrektor Izby jest w szczególności odpowiedzialny za:
            </p>
            <p> a)  realizację zadań Izby zgodnie ze Statutem oraz obowiązującym prawem,
            </p>
            <p> b)  wykonywanie Uchwał Walnego Zgromadzenia oraz zaleceń Rady Izby,
            </p>
            <p> c)  zawieranie i rozwiązywanie umów o pracę z pracownikami Izby,
            </p>
            <p> d)  zawieranie umów cywilnoprawnych dotyczących wykonywania określonych usług na rzecz
              Izby z osobami fizycznymi oraz podmiotami gospodarczymi, a także zakupu materiałów 
              eksploatacyjnych i sprzętu koniecznego do prawidłowego funkcjonowania Biura Izby po
              uprzednim uzgodnieniu z Radą Izby,
            </p>
            <p> e)  utrzymywanie stałej roboczej współpracy ze wszystkimi członkami Izby,
            </p>
            <p> f)   organizację działalności marketingowej i gospodarczej,
            </p>
            <p> g) przyznanie członkostwa oraz zawieszenie i usunięcie z listy członków.
            </p>
            <p> h) sygnowanie rocznego bilansu, sprawozdania finansowego i merytorycznego
            </p>
            <p> i)  prowadzenie prawidłowej gospodarki majątkowo-finansowej Izby,
            </p>
            <p> j) podpisywanie umów dot. prowadzenia rachunkowości oraz nadzór nad nią
            </p>
            <p> k) powoływanie funduszy celowych w porozumieniu z Prezydium Rady.
            </p>
            <p> l)  utrzymywanie roboczej współpracy z Radą Izby oraz komisjami stałymi i doraźnymi,
            </p>
            <p> ł)   dbałość o dobre imię i rozwój Izby.
            </p>
            <p> 3. Dyrektor Izby jest kierownikiem zakładu pracy /jednostki.
            </p>
            <p> 4. Dyrektor Izby działa na zasadach określonych regulaminem uchwalonym przez Radę
              Izby.
            </p>


            <h3> ROZDZIAŁ V
            </h3>
            <h4> Gospodarka majątkowo-finansowa Izby
            </h4>
            <h5> §28
            </h5>
            <p>1. Na majątek Izby składają się ruchomości, nieruchomości, środki niepieniężne oraz
              prawa majątkowe.</p>

            <p> 2. Majątek Izby powstaje m.in. z:
            </p>
            <p> A. składek członkowskich i wpisowego,
            </p>
            <p> B. wpływów z prowadzonej działalności gospodarczej,
            </p>
            <p> C. wpływów z działalności statutowej,
            </p>
            <p> D. wpływów z działalności odpłatnej,
            </p>
            <P> E. dotacji, darowizn, spadków i zapisów,
            </P>
            <P> F. dochodów z majątku Izby,
            </P>
            <P> G. przejęcia majątku w drodze fuzji, cesji lub innych porozumień zgodnie z przepisami
              prawa. 
            </P>
            <P> I. inne wpływy i dochody
            </P>

            <h5> § 29
            </h5>
            <p> 1.  Wysokość wszelkich opłat członkowskich ustala Walne Zgromadzenie Członków.
            </p>
            <p> 2.  Składka roczna może być płacona w czterech ratach kwartalnych lub innych ratach
              ustalonych przez Dyrektora Izby. Płatności odbywają się na podstawie not księgowych
              wystawianych przez Biuro Izby.
            </p>
            <h5> § 30
            </h5>
            <p> Członkowie Izby deklarują na rzecz Izby prowizję w wysokości 0,2% od transakcji
              handlowych, które zostały zawarte przy udziale lub poprzez wykorzystanie kontaktów
              zagranicznych uzyskanych za pośrednictwem Izby.
            </p>
            <h5> § 31
            </h5>
            <p> Oświadczenia woli w zakresie praw i obowiązków majątkowych Izby składają w jej imieniu
              Prezes Rady Izby/ lub w przypadku jego nieobecności lub choroby Wiceprezes i Dyrektor
              Izby, lub Dyrektor Izby i ustanowiony przez Radę Izby pełnomocnik.
            </p>
            <h5> §32
            </h5>
            <p> Szczegółowe zasady gospodarki finansowej Izby ustala Rada Izby.
            </p>  
            <h3>   ROZDZIAŁ VI
            </h3>
            <h4> Zmiana Statutu. Łączenie się i rozwiązywanie
            </h4>

            <h5> §33
            </h5>
            <p> 1.  Uchwała w sprawie zmiany Statutu Izby może być podjęta przez Walne Zgromadzenie
              większością 2/3 głosów oddanych, przy obecności co najmniej połowy uprawnionych do
              głosowania.
            </p>
            <p> 2.  W drugim terminie uchwała, o której mowa wyżej może być podjęta bez względu na
              liczbę obecnych członków.
            </p>
            <h5> §34
            </h5>
            <p> 1.  Uchwała w sprawie połączenia się Izb może być podjęta przez Walne Zgromadzenie
              zwykłą większością głosów oddanych przy obecności co najmniej połowy uprawnionych do
              głosowania w pierwszym terminie, a w drugim terminie może być podjęta bez względu na
              liczbę obecnych członków.
            </p>
            <p> 2.  Członkiem Izby jest podmiot gospodarczy - członek łączących się Izb akceptujący
              Statut i zobowiązujący się do płacenia składek członkowskich.
            </p>
            <h5> §35
            </h5>
            <p> 1.  Uchwała w sprawie rozwiązania Izby może być podjęta przez Walne Zgromadzenie
              większością 2/3 głosów oddanych przy obecności co najmniej połowy uprawnionych do
              głosowania w pierwszym terminie, a w drugim terminie może być podjęta bez względu na
              liczbę obecnych  członków.
            </p>
            <p> 2.  Uchwała w sprawie rozwiązania Izby powinna określać sposób przeprowadzenia
              likwidacji oraz cel, na jaki ma być użyty majątek Izby.
            </p> 3.  Walne Zgromadzenie po przyjęciu uchwały w sprawie rozwiązania Izby ustanowi
            likwidatora do przeprowadzenia postępowania likwidacyjnego zgodnie z obowiązującymi
            przepisami.


